package formatters;

public class M5 {
	public static void main(String[] args) {
		int i = 100;
		double j = 2.5;
		boolean k = true;
		String s1 = "abc";
		char c1 = 't';
		System.out.printf("i value is %d", i);
		System.out.printf("\nj value is %f", j);
		System.out.printf("\nk value is %b", k);
		System.out.printf("\n1 value is %s", s1);
		System.out.printf("\nc1 value is %c", c1);
	}
}
