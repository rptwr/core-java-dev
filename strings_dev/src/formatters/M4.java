package formatters;

public class M4 {
	public static void main(String[] args) {
		int i = 100;
		double j = 2.5;
		boolean k = true;
		String s1 = "abc";
		char c1 = 't';
		System.out.printf("i value is %d", i);
		System.out.printf(" j value is %f", j);
		System.out.printf(" k value is %b", k);
		System.out.printf(" s1 value is %s", s1);
		System.out.printf(" c1 value is %c", c1);
	}
}
